public class cell {
    private int x;
    private int  y;

    public cell() {
        int x = 400;
        int  y = 600;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}

